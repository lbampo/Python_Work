# Dictionary Basics

# Define dictionary story1, with keys - start - middle - end

story1 = {
    'start' : 'This is where the story begins',
    'middle' : 'They like calves, I like shins',
    'end' : 'This is where the story ends',
    'conclusion' : 'We are different, but lets be friends '
}
#Print dictionary
print(story1)

# Print type of dictionary
print(type(story1))

# Print only the keys
print(story1.keys())

#Print only the values
print(story1.values())

print(story1['start'])
print(story1['middle'])
print(story1['end'])
print(story1['conclusion'])
