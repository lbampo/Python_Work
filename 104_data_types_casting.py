# CASTING

# Casting is when you change an object data to a specific data type

# String to integer
my_var = '10'
print(type(my_var))

my_casted_var = int(my_var)
print(my_casted_var)

# Integer/floats to string
my_int_var = 14
my_casted_str = str(my_int_var)

print('my_casted_str', type(my_casted_str))






